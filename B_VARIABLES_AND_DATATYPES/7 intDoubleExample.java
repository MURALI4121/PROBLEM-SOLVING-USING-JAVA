package B_VARIABLES_AND_DATATYPES;

class intDoubleExample {

    public static void main(String[] args) {
        int num1 = 15;
        int num2 = 3;
        int result = num1/num2;

        System.out.println("Division using int :" + num1 + "/" + num2 + " = "+result);

        double val1 = 15.25;
        double val2 = 2.221548;
        double Result = val1/val2;

        System.out.println("Division using double : " + val1 + "/" +val2 + " = " + Result );
    }
}

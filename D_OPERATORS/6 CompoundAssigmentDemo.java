package D_OPERATORS;

class CompoundAssignmentDemo {

    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        a += b;
        System.out.println(a);

        a -= b;
        System.out.println(a);

        a *= b;
        System.out.println(a);

        a /= b;
        System.out.println(a);
    }
}

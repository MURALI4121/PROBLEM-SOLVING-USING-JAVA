package A_BASIC_SYNTAX_AND_STRUCTURE;

class Comments {

    public static void main(String[] args) {


        // SINGLE LINE COMMENTS

        /* MULTILINE COMMENTS
        *---------------
        * ----------------
        * ----------------
         */

       //Single-line comment: declare two numbers to add them

        int number1 = 5; //this is number 1
        int number2 = 6; //this is number 2

        /* Multiline comment
        * add the two numbers
        * formula add = a + b;
        */

        int add = number1 + number2; // this will add two numbers

        // this line will print two numbers
        System.out.println("the addition of two numbers is : " + add);
    }
}
